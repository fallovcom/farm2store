            },
            hair_care: {
                icon: iconBase + 'hair-care.svg'
            },
            hardware_store: {
                icon: iconBase + 'hardware-store.svg'
            },
            home_goods_store: {
                icon: iconBase + 'home-goods-store.svg'
            },
            jewelry_store: {
                icon: iconBase + 'jewelry-store.svg'
            },
            book_store: {
                icon: iconBase + 'book-store.svg'
            },
            pet_store: {
                icon: iconBase + 'pet-store.svg'
            },
            pharmacy: {
                icon: iconBase + 'pharmacy.svg'
            },
            shoe_store: {
