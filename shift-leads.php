        function handleLocationError(browserHasGeolocation, pos) {}

        function initMap() {
            pos = new google.maps.LatLng(17.5029348, 78.5644666);
            map = new google.maps.Map(document.getElementById("map"), {
                center: pos,
                zoom: 15,
                gestureHandling: 'none',
                zoomControl: false,
                disableDefaultUI: true,
                streetViewControl: true
            });
            const card = document.getElementById("pac-card");
            const input = document.getElementById("pac-input");
            const geocoder = new google.maps.Geocoder();

            function searchBusiness() {
                if (input.value.length == 0) {
                    locateMe();
                } else {
                    service = new google.maps.places.PlacesService(map);
                    service.nearbySearch(request, callback);
                }
            }

            function geocodeLatLng(geocoder, map, latlng, input) {
                geocoder.geocode({
                    location: latlng
                }, (results, status) => {
                    if (status === "OK") {
                        if (results[0]) {
                            input.value = results[0].formatted_address;
                        } else {
                            console.error("No results found");
                        }
                    } else {
                        console.error("Geocoder failed due to: " + status);
                    }
                });
            }

            function locateMe() {
                if (navigator.geolocation) {
                    navigator.geolocation.getCurrentPosition(function(position) {
                        pos = {
                            lat: position.coords.latitude,
                            lng: position.coords.longitude
                        };
                        request.location = pos;
                        geocodeLatLng(geocoder, map, pos, input);
                        searchBusiness();
                    }, function() {
                        handleLocationError(true, map.getCenter());
                    });
                } else {
                    handleLocationError(false, map.getCenter());
                }
            }
			
			locateMe();
			map.controls[google.maps.ControlPosition.TOP_RIGHT].push(card);
			const autocomplete = new google.maps.places.Autocomplete(input, {
				types: ['address'],
				componentRestrictions: {
					country: <?php $n = strtolower($_SERVER["HTTP_CF_IPCOUNTRY"]);echo '"'.$n.'"'; ?>
				}
			});
			autocomplete.bindTo("bounds", map);
			autocomplete.setFields(["address_components", "geometry", "icon", "name"]);
			const marker = new google.maps.Marker({
				map,
				anchorPoint: new google.maps.Point(0, -29)
			});
			autocomplete.addListener("place_changed", () => {
				marker.setVisible(false);
				const place = autocomplete.getPlace();
				if (input.value.length == 0 || !place.geometry) {
					locateMe();
					return;
				}
				request.location = place.geometry.location;
				marker.setPosition(place.geometry.location);
				marker.setVisible(true);
				searchBusiness();
			});
						
            function setupClickListener(id, types) {
                const radioButton = document.getElementById(id);
                radioButton.addEventListener("click", () => {
                    autocomplete.setTypes([]);
                    request.type = types;
                    searchBusiness();
                });
            }
            setupClickListener("changetype-store", ["store"]);
            setupClickListener("changetype-clothing_store", ["clothing_store"]);
            setupClickListener("changetype-electronics_store", ["electronics_store"]);
            setupClickListener("changetype-furniture_store", ["furniture_store"]);
            setupClickListener("changetype-hair_care", ["hair_care"]);
            setupClickListener("changetype-hardware_store", ["hardware_store"]);
            setupClickListener("changetype-jewelry_store", ["jewelry_store"]);
            setupClickListener("changetype-book_store", ["book_store"]);
            setupClickListener("changetype-pet_store", ["pet_store"]);
            setupClickListener("changetype-pharmacy", ["pharmacy"]);
            setupClickListener("changetype-shopping_mall", ["shopping_mall"]);
        }

        function callback(results, status) {
            for (let i = 0; i < markers.length; i++) {
                markers[i].setMap(null);
            }
            markers = [];
            if (status === google.maps.places.PlacesServiceStatus.OK) {
                for (let i = 0; i < results.length; i++) {
                    createMarker(results[i]);
                }
                map.setCenter(results[0].geometry.location);
                map.setZoom(17);
            } else {
                alert("No results");
				return;
            }
        }

        function createMarker(place) {
            const marker = new google.maps.Marker({
                map,
                position: place.geometry.location,
                icon: {
                    url: icons[request.type[0]].icon,
                    scaledSize: new google.maps.Size(34, 34)
                }
            });
            markers.push(marker);
            marker.addListener("click", () => {
                map.setZoom(17);
                map.setCenter(marker.getPosition());
                service.getDetails({
                    placeId: place.place_id
                }, function(p, status) {
                    if (status === google.maps.places.PlacesServiceStatus.OK && (p.website || p.formatted_phone_number || p.international_phone_number)) {
                        output += p.name + ",";
						output += (p.website? p.website : "") + ",";
						output += (p.formatted_phone_number? p.formatted_phone_number.replaceAll("+", "").replaceAll(" ", "") : "") + ",";
						output += p.international_phone_number? "https://wa.me/" + p.international_phone_number.replaceAll("+", "").replaceAll(" ", "") : "";
						output += "\r\n";
                    }
                });
            });
        }
		
		let WAIT_TIME = 10000;

		function saveLeads() {
			var blob = new Blob([output], { type: "text/csv;charset=utf-8" });
			saveAs(blob, document.getElementById("pac-input").value+" - "+fileName+".csv");
			output = "name,website,mobile,whatsapp\r\n";
		}
		
		async function clickAndSleep(s) {
			await sleep(WAIT_TIME);
			output = "name,website,mobile,whatsapp\r\n";
			fileName = s + "-" + (new Date()).getTime();
			document.getElementById("changetype-"+s).click();
			await sleep(WAIT_TIME);
			for(let j = 0; j < markers.length; j++) {
				new google.maps.event.trigger( markers[j], 'click' );
				await sleep(3000);
			}
			await sleep(WAIT_TIME);
			saveLeads();
			await sleep(WAIT_TIME);
		}
		
		function sleep(ms) {
			return new Promise(resolve => setTimeout(resolve, ms));
		}
